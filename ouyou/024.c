/*
入力された整数の範囲内で素数を表示する
問題文：
ユーザーから2つの整数（範囲の開始値と終了値）を入力し、その範囲内の素数をすべて表示するプログラムを作成しなさい。
素数とは：

1より大きい自然数で、1とその数自身以外では割り切れない数です。
ヒント：

1つのfor文で範囲内の各数を調べ、内部で条件を確認するためにif文ともう1つのfor文を使います（ただし今回は内部のループは制限）。
数が素数かどうかを確認する方法を考えましょう。
*/
#include <stdio.h>

int main(void)
{
    int start, end; // 範囲の開始値と終了値

    printf("範囲の開始値を入力してください：");
    scanf("%d", &start);
    printf("範囲の終了値を入力してください：");
    scanf("%d", &end);

    printf("%dから%dまでの素数は以下の通りです：\n", start, end);

    for (int n = start; n <= end; n++)
    {
        int is_prime = 1; // 素数フラグ（1: 素数、0: 素数でない）

        // nが素数かどうかを判定する処理をここに記述

        if (is_prime && n > 1)
            printf("%d ", n);
    }

    printf("\n");

    return 0;
}
